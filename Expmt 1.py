#FUNCTION
def add(x, y):
    z = x + y
    print("Addition:", z)

def subtract(x, y):
    z = x - y
    print("Subtraction:", z)

def multiply(x, y):
    z = x * y
    print("Multiplication:", z)

def divide(x, y):
    if y != 0:
        z = x / y
        print("Division:", z)
    else:
        print("Cannot divide by zero")

